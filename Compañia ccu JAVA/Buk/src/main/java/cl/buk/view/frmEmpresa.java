    private void btnCrearActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCrearActionPerformed
        // TODO add your handling code here:
        try {
            int indiceUbicacion = cbxUbicacion.getSelectedIndex()+1;
            Ubicacion ubicacion = controllerUbicacion.findUbicacion(indiceUbicacion);
                        
            empresa = new Empresa();
            empresa.setRut(Integer.valueOf(txtRut.getText()));
            empresa.setDv(txtDv.getText());
            empresa.setRazonSocial(txtRazonSocial.getText());
            empresa.setIdUbicacion(ubicacion);
            
            controllerEmpresa.create(empresa);
            
            JOptionPane.showMessageDialog(this, "Empresa" + empresa.getRazonSocial() + " ha si creada satisfactoriamente", "Confirmación", JOptionPane.INFORMATION_MESSAGE);
            CargarEmpresas();
            LimpiarControles();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnCrearActionPerformed

    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        // TODO add your handling code here:
        try {
            int rut = Integer.valueOf(txtRut.getText());
            empresa = controllerEmpresa.findEmpresa(rut);
            if (empresa != null) {
                txtRut.setEnabled(false);
                txtDv.setEnabled(false);
                txtDv.setText(empresa.getDv());
                txtRazonSocial.setText(empresa.getRazonSocial());
                if (empresa.getIdUbicacion() != null){
                    cbxUbicacion.setSelectedItem(empresa.getIdUbicacion().imprimir());
                } else {
                    cbxUbicacion.setSelectedIndex(-1);
                }
            } else {
                JOptionPane.showMessageDialog(this, "Rut ingresado no existe", "Validación", JOptionPane.WARNING_MESSAGE);
                LimpiarControles();
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

    private void btnActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarActionPerformed
        // TODO add your handling code here:
        try {
            int rut = Integer.valueOf(txtRut.getText());
            empresa = controllerEmpresa.findEmpresa(rut);
            if (empresa != null) {
                empresa.setRazonSocial(txtRazonSocial.getText());
                controllerEmpresa.edit(empresa);
                
                JOptionPane.showMessageDialog(this, "Empresa " + empresa.getRazonSocial() + " editada satisfactoriamente", "Confirmación", JOptionPane.INFORMATION_MESSAGE);
                
                CargarEmpresas();
                LimpiarControles();
            } else {
                JOptionPane.showMessageDialog(this, "Rut ingresado no existe", "Validación", JOptionPane.WARNING_MESSAGE);
                LimpiarControles();
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnActualizarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // TODO add your handling code here:
        try {
            int rut = Integer.valueOf(txtRut.getText());
            empresa = controllerEmpresa.findEmpresa(rut);
            if (empresa != null) {
                controllerEmpresa.destroy(rut);
                
                JOptionPane.showMessageDialog(this, "Empresa " + empresa.getRazonSocial() + " eliminada satisfactoriamente", "Confirmación", JOptionPane.INFORMATION_MESSAGE);
                
                CargarEmpresas();
                LimpiarControles();
            } else {
                JOptionPane.showMessageDialog(this, "Rut ingresado no existe", "Validación", JOptionPane.WARNING_MESSAGE);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

    private void LimpiarControles() {
        // TODO add your handling code here:
        txtRut.setEnabled(true);
        txtDv.setEnabled(true);
        txtRut.setText("");
        txtDv.setText("");
        txtRazonSocial.setText("");
    }


