    private void CargarEmpresas() {
        EmpresaTableModel model = new EmpresaTableModel(controllerEmpresa.findEmpresaEntities());
        tblEmpresas.setModel(model);
    }

    private void CargarUbicacion() {
        List<Ubicacion> ubicaciones = controllerUbicacion.findUbicacionEntities();
        String item = "";
        for (Ubicacion u : ubicaciones) {
            item = u.getCalle() + " #" + u.getNumero() + ", " + u.getRegion();
            cbxUbicacion.addItem(item);
        }
    }
