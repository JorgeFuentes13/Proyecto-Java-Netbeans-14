    private void btnCrearActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCrearActionPerformed
        // TODO add your handling code here:
        try {
            int rut = Integer.valueOf(cbxEmpresa.getSelectedItem().toString());
            Empresa empresa = controllerEmpresa.findEmpresa(rut);
            
            int indiceUbicacion = cbxUbicacion.getSelectedIndex()+1;
            Ubicacion ubicacion = controllerUbicacion.findUbicacion(indiceUbicacion);
            
            departamento = new Departamento();
            departamento.setNombre(txtNombre.getText());
            departamento.setSigla(txtSigla.getText());
            departamento.setRutEmpresa(empresa);
            departamento.setIdUbicacion(ubicacion);
            
            controllerDepartamento.create(departamento);
            
            LimpiarControles();
            CargarEmpresas();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnCrearActionPerformed


