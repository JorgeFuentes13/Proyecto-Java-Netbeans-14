    private void CargarEmpresas() {
         List<Empresa> empresas = controllerEmpresa.findEmpresaEntities();
        String item = "";
        for (Empresa e : empresas) {
            item = e.getRut().toString();
            cbxEmpresa.addItem(item);
        }
    }

    private void CargarDepartamentos() {
        DepartamentoTableModel model = new DepartamentoTableModel(controllerDepartamento.findDepartamentoEntities());
        tblDepartamentos.setModel(model);
    }
    
    private void CargarUbicacion() {
        List<Ubicacion> ubicaciones = controllerUbicacion.findUbicacionEntities();
        String item = "";
        for (Ubicacion u : ubicaciones) {
            item = u.getCalle() + " #" + u.getNumero() + ", " + u.getRegion();
            cbxUbicacion.addItem(item);
        }
    }

    private void LimpiarControles() {
        txtNombre.setText("");
        txtSigla.setText("");
        cbxEmpresa.setSelectedIndex(-1);
        cbxUbicacion.setSelectedIndex(-1);
    }
