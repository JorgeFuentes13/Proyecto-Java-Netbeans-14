    private void btnCrearActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCrearActionPerformed
        // TODO add your handling code here:
        try {
            int idDepartamento = cbxDepartamento.getSelectedIndex()+1;
            Departamento departamento = controllerDepartamento.findDepartamento(idDepartamento);
            
            empleado = new Empleado();
            empleado.setRut(Integer.valueOf(txtRut.getText()));
            empleado.setDv(txtDv.getText());
            empleado.setNombre(txtNombre.getText());
            empleado.setApellidos(txtApellidos.getText());
            empleado.setCargo(txtCargo.getText());
            empleado.setSalario(Integer.valueOf(txtSalario.getText()));
            empleado.setIdDepartamento(departamento);
            
            controllerEmpleado.create(empleado);
            
            if (rbtContrato.isSelected()) {
                
            } else if (rbtHonorario.isSelected()) {
                int rut = Integer.valueOf(txtRut.getText());
                Empleado empleado = controllerEmpleado.findEmpleado(rut);
                
                honorario = new Honorario();
                honorario.setValorHora(Integer.valueOf(txtValorHora.getText()));
                honorario.setRutEmpleado(empleado);
                
                controllerHonorario.create(honorario);
            }

            JOptionPane.showMessageDialog(this, "Empleado ha si creada satisfactoriamente", "Confirmación", JOptionPane.INFORMATION_MESSAGE);
            CargarEmpleados();
            LimpiarControles();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnCrearActionPerformed


