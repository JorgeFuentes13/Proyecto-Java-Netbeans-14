    private void ValidaTipoContrato() {
        if (rbtContrato.isSelected()) {
            lblValorHora.setEnabled(false);
            txtValorHora.setEnabled(false);
        } else if (rbtHonorario.isSelected()) {
            lblValorHora.setEnabled(true);
            txtValorHora.setEnabled(true);
        }
    }

    private void LimpiarControles() {
        txtRut.setText("");
        txtDv.setText("");
        txtNombre.setText("");
        txtApellidos.setText("");
        txtCargo.setText("");
        txtSalario.setText("");
        txtSalario.setText("");
        cbxDepartamento.setSelectedIndex(-1);
    }

    private void CargarEmpleados() {
        EmpleadoTableModel model = new EmpleadoTableModel(controllerEmpleado.findEmpleadoEntities());
        tblEmpleado.setModel(model);
    }

    private void CargarDepartamento() {
        List<Departamento> departamentos = controllerDepartamento.findDepartamentoEntities();
        String item = "";
        for (Departamento d : departamentos) {
            item = d.getNombre();
            cbxDepartamento.addItem(item);
        }
    }
